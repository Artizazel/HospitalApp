    public GiveMedicine() {
        initComponents();
        
        
        DefaultListModel DLM = new DefaultListModel();
        
        for (int i = 0; i < SystemDatabase.prescriptions.size(); i++) {
            if(SystemDatabase.prescriptions.get(i).getRecieved() == false)
            {
            DLM.addElement("Patient: " + SystemDatabase.prescriptions.get(i).getPatientID() + "  Doctor: " + SystemDatabase.prescriptions.get(i).getDocID() + "  Medicine: " + SystemDatabase.prescriptions.get(i).getMedName() + "["+SystemDatabase.prescriptions.get(i).getQuantity()+"]"  );
            
            tempPres.add(SystemDatabase.prescriptions.get(i));
            }
        }
        
        
        lstPrescriptions.setModel(DLM);
        
    }

