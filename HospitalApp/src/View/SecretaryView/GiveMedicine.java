    private void btnMedGivenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMedGivenActionPerformed
        // TODO add your handling code here:
        
        
        Medicine currentMed = SystemDatabase.findMed(tempPres.get(lstPrescriptions.getSelectedIndex()).getMedName());
        
        
        
        if(currentMed.getStock() >= tempPres.get(lstPrescriptions.getSelectedIndex()).getQuantity())
        {
        tempPres.get(lstPrescriptions.getSelectedIndex()).setRecieved(true);        
        currentMed.setStock(currentMed.getStock()-tempPres.get(lstPrescriptions.getSelectedIndex()).getQuantity());
        SystemDatabase.SaveDatabase();
        new SecretaryHomepage().setVisible(true);
        this.dispose();
        }
        else
        {
            showMessageDialog(null, "Not Enough Medicine");
        }
        
        
        
        
    }//GEN-LAST:event_btnMedGivenActionPerformed

